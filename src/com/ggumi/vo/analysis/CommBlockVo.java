package com.ggumi.vo.analysis;

public class CommBlockVo {
	
	private int comm_code;
	private int h_code;
	private String comm_name;
	private String lat;
	private String lng;
	private int population_no;
	public int getComm_code() {
		return comm_code;
	}
	public void setComm_code(int comm_code) {
		this.comm_code = comm_code;
	}
	public int getH_code() {
		return h_code;
	}
	public void setH_code(int h_code) {
		this.h_code = h_code;
	}
	public String getComm_name() {
		return comm_name;
	}
	public void setComm_name(String comm_name) {
		this.comm_name = comm_name;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public int getPopulation_no() {
		return population_no;
	}
	public void setPopulation_no(int population_no) {
		this.population_no = population_no;
	}
	
	

}
